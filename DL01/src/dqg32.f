      SUBROUTINE DQG32(XL,XU,FCT,Y)
      IMPLICIT NONE
C SUBROUTINE DQG32 COMPUTES INTEGRAL(FCT(X),SUMMED OVER X FROM XL TO XU)
C USING 32-POINT GAUSSIAN QUADRATURE FORMULA, WHICH INTEGRATES
C POLYNOMIALS OF UP TO DEGREE 63 EXACTLY
C THIS SUBROUTINE IS COPIED FROM IBM SSP
C USAGE: CALL DQG32(XL,XU,FCT,Y)
